package com.ll.model.service;

public class CalcService {

    public int sum(int a, int b){
        return a+b;
    }

    public int substract(int a, int b) {
        return a-b;
    }

    public int multi(int a, int b) {
        return a*b;
    }

    public int divide(int a, int b) {
        return a/b;
    }
}
